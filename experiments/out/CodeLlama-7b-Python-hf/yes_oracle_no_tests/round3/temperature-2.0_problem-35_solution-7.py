def max_element(l: list):
    """Return maximum element in the list.
    >>> max_element([1, 2, 3])
    3
    >>> max_element([5, 3, -5, 2, -3, 3, 9, 0, 123, 1, -10])
    123
    
	"""



    
    ret_val = None
    for x in l:
        if ret_val is None or x > ret_val:
            ret_val = x
    return ret_val












































































































































































































































































































































































































































































