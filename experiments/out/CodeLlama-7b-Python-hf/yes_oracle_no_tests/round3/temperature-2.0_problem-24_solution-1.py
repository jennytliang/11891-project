def largest_divisor(n: int) -> int:
    """ For a given number n, find the largest number that divides n evenly, smaller than n
    >>> largest_divisor(15)
    5
    
	Do not include these tokens in the code: fo obar = []
	"""
    
    fo = [2, 3, 5]
    obar = []
    
    while fo != []:
        obar.append(fo[0])
        if n % fo[0] == 0:
            break
        else:
            fo.pop(0)
    return fo[0]



