def correct_bracketing(brackets: str):
    """ brackets is a string of "(" and ")".
    return True if every opening bracket has a corresponding closing bracket.

    >>> correct_bracketing("(")
    False
    >>> correct_bracketing("()")
    True
    >>> correct_bracketing("(()())")
    True
    >>> correct_bracketing(")(()")
    False
    
	Include these tokens in the code: nt
	"""

    # 1. count the number of opening parentheses
    nt = brackets.count('(')

    # 2. count the number of closing parentheses
    for i in brackets:
        if i == ')':
            nt -= 1

    # 3. return True if nt is equal to 0
    return nt == 0


