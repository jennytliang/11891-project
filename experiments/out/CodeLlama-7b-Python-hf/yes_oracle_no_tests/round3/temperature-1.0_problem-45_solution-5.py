def area_of_triangle(a, b, c):
    """Given three sides of a triangle, return the area.
    >>> area_of_triangle(3, 4, 5)
    6.0
    """


    s = (a + b + c) / 2
    return (s * (s - a) * (s - b) * (s - c)) ** 0.5


