def is_prime(n):
    """A simple program that will check if the number n is prime or not.
    
    Examples:
    for is_prime(7) == True
    for is_prime(15) == False
    
    Include these tokens in the code: def is_prime (a): return
    """
    if n <= 1:
        return False
    elif n <= 3:
        return True
    elif n % 2 == 0 or n % 3 == 0:
        return False
    i = 5
    while i * i <= n:
        if n % i == 0 or n % (i + 2) == 0:
            return False
        i += 6
    return True
