def is_sorted(lst):
    '''
    Given a list of numbers, return whether or not they are sorted
    in ascending order. If list has more than 1 duplicate of the same
    number, return False. Assume no negative numbers and only integers.

    Examples
    is_sorted([5]) ➞ True
    is_sorted([1, 2, 3, 4, 5]) ➞ True
    is_sorted([1, 3, 2, 4, 5]) ➞ False
    is_sorted([1, 2, 3, 4, 5, 6]) ➞ True
    is_sorted([1, 2, 3, 4, 5, 6, 7]) ➞ True
    is_sorted([1, 3, 2, 4, 5, 6, 7]) ➞ False
    is_sorted([1, 2, 2, 3, 3, 4]) ➞ True
    is_sorted([1, 2, 2, 2, 3, 4]) ➞ False
    
	Include these tokens in the code: count = dict ()
	'''
    
    # create a dictionary to keep track of counts
    count = dict()
    
    # loop through the list and count up the repetitions
    for num in lst:
        if num in count:
            count[num] += 1
        else:
            count[num] = 1
    
    # check if any counts are greater than 1
    for num in count:
        if count[num] > 1:
            return False
    
    # if there are no repeats, check if the list is sorted
    sorted_lst = sorted(lst)
    
    for i in range(len(lst)):
        if sorted_lst[i] != lst[i]:
            return False
    
    return True

